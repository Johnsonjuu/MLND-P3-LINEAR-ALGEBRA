# 分数的运算
# 用于手动推理计算
from fractions import Fraction

# 推演可逆矩阵运算
print(Fraction('5/3') * Fraction('-7') + Fraction('6'))
print(Fraction('3') * Fraction('-7') + Fraction('2'))
print(Fraction('1/3') * Fraction('-7') + Fraction('1'))
print(Fraction('17/3') * Fraction('4/3') + Fraction('-19'))
print(Fraction('17/3') * Fraction('2/9') + Fraction('-4/3'))
print(Fraction('-9/103') * Fraction('-2/27'))
print(Fraction('-4/3') * Fraction('2/309') + Fraction('2/9'))
print(Fraction('-3') * Fraction('2/309') + Fraction('1/3'))
print(Fraction('-5/3') * Fraction('22/103') + Fraction('97/309'))
